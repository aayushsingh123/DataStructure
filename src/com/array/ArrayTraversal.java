package com.array;

public class ArrayTraversal {
	
	public static void main (String [] args) {
		
		
		int[] array= {1,2,3,4,5,6};
		for(int i=0; i<array.length; i++) {
			
			System.out.println(array[i]);
			
		}
		
	}

}

/*
int[] array = {1, 2, 3, 4, 5, 6}; ek integer array ko represent karta hai. Iska matlab hai:

int[]: Yeh declare karta hai ki array ek integer array hai, jo multiple integer values ko store kar sakta hai.
array: Yeh variable ka naam hai, jisme values store ho rahi hain.
{1, 2, 3, 4, 5, 6}: Yeh array ke elements hain. Is case mein, array mein total 6 values hain: 1, 2, 3, 4, 5, 6.

*/